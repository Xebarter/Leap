"use client"

import type React from "react"

import { createClient } from "@/lib/supabase/client"
import { Button } from "@/components/ui/button"
import { Card, CardContent, CardDescription, CardHeader, CardTitle } from "@/components/ui/card"
import { Input } from "@/components/ui/input"
import { Label } from "@/components/ui/label"
import Link from "next/link"
import { useRouter, useSearchParams } from "next/navigation"
import { useState, useEffect } from "react"
import { Calendar, CheckCircle2 } from "lucide-react"

export default function SignUpPage() {
  const [email, setEmail] = useState("")
  const [password, setPassword] = useState("")
  const [fullName, setFullName] = useState("")
  const [isAdmin, setIsAdmin] = useState(false)
  const [error, setError] = useState<string | null>(null)
  const [isLoading, setIsLoading] = useState(false)
  const router = useRouter()
  const searchParams = useSearchParams()
  const [pendingVisit, setPendingVisit] = useState<any>(null)

  // Check for pending visit and redirect URL
  useEffect(() => {
    const action = searchParams.get('action')
    if (action === 'schedule-visit') {
      const storedVisit = localStorage.getItem('pendingVisit')
      if (storedVisit) {
        setPendingVisit(JSON.parse(storedVisit))
      }
    }
  }, [searchParams])

  const handleSignUp = async (e: React.FormEvent) => {
    e.preventDefault()
    setIsLoading(true)
    setError(null)

    try {
      const res = await fetch("/api/auth/signup", {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
        },
        body: JSON.stringify({
          email,
          password,
          fullName,
          isAdmin,
        }),
      })

      const body = await res.json()

      if (!res.ok) {
        setError(body.error || "An error occurred while signing up.")
        setIsLoading(false)
        return
      }

      const { user, session } = body.data ?? {}

      if (user && session) {
        // Check for redirect URL from visit scheduling
        const redirectUrl = searchParams.get('redirect')
        const action = searchParams.get('action')
        
        if (redirectUrl && action === 'schedule-visit') {
          // Redirect back to property page
          router.push(decodeURIComponent(redirectUrl))
          router.refresh()
          return
        }

        const redirectPath = isAdmin ? '/admin' : '/tenant'
        router.push(redirectPath)
        router.refresh()
      } else {
        router.push("/auth/verify-email")
      }

      setIsLoading(false)
    } catch (err: any) {
      const message = err?.message || "An error occurred"
      if (message === 'Failed to fetch' || message.includes('fetch')) {
        setError("Unable to connect to authentication service. Please check your internet connection.")
      } else {
        setError(message)
      }
      setIsLoading(false)
    }
  }

  const handleGoogleSignUp = () => {
    setIsLoading(true)
    setError(null)

    const supabase = createClient()
    
    supabase.auth.signInWithOAuth({
      provider: 'google',
      options: {
        redirectTo: `${window.location.origin}/auth/callback`,
      }
    }).then((response) => {
      if (response.error) {
        const message = response.error.message || "An error occurred"
        setError(message)
        setIsLoading(false)
      }
    }).catch((err: any) => {
      const message = err?.message || "An error occurred"
      if (message === 'Failed to fetch' || message.includes('fetch')) {
        setError("Unable to connect to authentication service. Please check your internet connection.")
      } else {
        setError(message)
      }
      setIsLoading(false)
    })
  }

  return (
    <div className="flex min-h-svh w-full items-center justify-center p-6 md:p-10">
      <div className="w-full max-w-sm">
        {pendingVisit && (
          <div className="mb-4 bg-primary/10 border border-primary/20 rounded-lg p-4">
            <div className="flex items-start gap-3">
              <div className="h-10 w-10 rounded-full bg-primary/20 flex items-center justify-center flex-shrink-0">
                <Calendar className="h-5 w-5 text-primary" />
              </div>
              <div className="space-y-1 flex-1">
                <p className="text-sm font-medium">Almost there! Create your account</p>
                <p className="text-xs text-muted-foreground">
                  Sign up to schedule your visit to <strong>{pendingVisit.propertyTitle}</strong>
                </p>
              </div>
            </div>
          </div>
        )}
        <Card>
          <CardHeader>
            <CardTitle className="text-2xl">
              {pendingVisit ? 'Create Tenant Account' : 'Join Leap'}
            </CardTitle>
            <CardDescription>
              {pendingVisit 
                ? 'Create your account to schedule property visits and more'
                : 'Create an account to start renting'
              }
            </CardDescription>
          </CardHeader>
          <CardContent>
            <form onSubmit={handleSignUp} className="space-y-4">
              <div className="grid gap-2">
                <Label htmlFor="fullName">Full Name</Label>
                <Input id="fullName" required value={fullName} onChange={(e) => setFullName(e.target.value)} />
              </div>
              <div className="grid gap-2">
                <Label htmlFor="email">Email</Label>
                <Input id="email" type="email" required value={email} onChange={(e) => setEmail(e.target.value)} />
              </div>
              <div className="grid gap-2">
                <Label htmlFor="password">Password</Label>
                <Input
                  id="password"
                  type="password"
                  required
                  value={password}
                  onChange={(e) => setPassword(e.target.value)}
                />
              </div>
              {!pendingVisit && (
                <div className="flex items-center gap-2">
                  <input type="checkbox" id="isAdmin" checked={isAdmin} onChange={(e) => setIsAdmin(e.target.checked)} />
                  <Label htmlFor="isAdmin">Register as Admin</Label>
                </div>
              )}
              {error && <p className="text-sm text-red-500">{error}</p>}
              <Button type="submit" className="w-full" disabled={isLoading}>
                {isLoading ? "Creating account..." : "Sign Up"}
              </Button>

              <div className="relative">
                <div className="absolute inset-0 flex items-center">
                  <span className="w-full border-t" />
                </div>
                <div className="relative flex justify-center text-xs uppercase">
                  <span className="bg-background px-2 text-muted-foreground">
                    Or continue with
                  </span>
                </div>
              </div>

              <Button 
                type="button" 
                variant="outline" 
                className="w-full" 
                onClick={handleGoogleSignUp}
                disabled={isLoading}
              >
                <svg className="mr-2 h-4 w-4" viewBox="0 0 24 24">
                  <path
                    d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
                    fill="#4285F4"
                  />
                  <path
                    d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
                    fill="#34A853"
                  />
                  <path
                    d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
                    fill="#FBBC05"
                  />
                  <path
                    d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
                    fill="#EA4335"
                  />
                </svg>
                Google
              </Button>

              <div className="text-center text-sm">
                Already have an account?{" "}
                <Link 
                  href={pendingVisit 
                    ? `/auth/login?redirect=${searchParams.get('redirect')}&action=schedule-visit`
                    : "/auth/login"
                  } 
                  className="underline underline-offset-4"
                >
                  Login
                </Link>
              </div>
            </form>
          </CardContent>
        </Card>
      </div>
    </div>
  )
}
